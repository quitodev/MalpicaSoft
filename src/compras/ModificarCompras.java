    private void botonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsultarActionPerformed
        // Obtengo los datos de los campos de texto
        datoLetraComprob = campoLetraComprob.getText().trim();
        datoPuntoVenta = campoPuntoVenta.getText().trim();
        datoNroComprob = campoNroComprob.getText().trim();
        datoNroFactura = campoLetraComprob.getText().trim() + "-" + campoPuntoVenta.getText().trim() + "-" + campoNroComprob.getText().trim();

        // Continúa si el número de factura contiene todos los dígitos
        if (datoNroFactura.length() == 15) {

            try {
                // Establece conexión con DB para consultar la factura ingresada
                Connection con = Conexion.conectar();
                PreparedStatement sta = con.prepareStatement("select * from compras where nro_factura = '" + datoNroFactura + "'");
                ResultSet res = sta.executeQuery();

                if (res.next()) {
                    // Si la factura ya fue creada, obtiene el resto de los datos
                    ID = res.getInt("id");
                    campoFechaCompra.setText(res.getString("fecha_factura"));
                    campoNroFactura1.setText(res.getString("nro_factura"));
                    campoCodigoProv.setText(res.getString("codigo"));
                    campoNombreProv.setText(res.getString("razon_social"));
                    campoCondicionProv.setText(res.getString("condicion"));
                    campoCodigo.setText(res.getString("codigo_stock"));
                    campoDescripcion.setText(res.getString("descripcion_stock"));
                    campoCantidad.setText(res.getString("cantidad"));
                    campoPrecioUnit.setText(res.getString("precio_unit"));
                    campoImpuestos.setText(res.getString("impuestos"));
                    campoPrecioFinal.setText(res.getString("precio_total"));

                    // Oculta botones, guarda el código del producto y oculta mensaje de error                    
                    botonConsultar.setVisible(false);
                    botonModificar.setVisible(true);
                    botonGuardar.setVisible(false);

                    labelMensaje.setVisible(false);

                    datoCodigo1 = campoCodigo.getText().trim();

                    botonLimpiar.requestFocus();

                    con.close();

                } else {
                    // Muestra un mensaje de error si la factura ingresada no existe en la DB 
                    labelMensaje.setForeground(new java.awt.Color(255, 0, 0));
                    labelMensaje.setText("La factura ingresada no existe! Intente nuevamente");
                    labelMensaje.setVisible(true);

                    campoLetraComprob.requestFocus();

                    con.close();
                }

            } catch (SQLException e) {
                // Si no se conecta con la DB, muestra un mensaje de error
                JOptionPane.showConfirmDialog(null, "Error de conexión: Base compras!", "Información", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            }

        } else {
            // Si no se completaron los campos correctamente, muestra un mensaje de error
            labelMensaje.setForeground(new java.awt.Color(255, 0, 0));
            labelMensaje.setText("Por favor, revise los datos ingresados!");
            labelMensaje.setVisible(true);

            campoLetraComprob.requestFocus();
        }
    }//GEN-LAST:event_botonConsultarActionPerformed

