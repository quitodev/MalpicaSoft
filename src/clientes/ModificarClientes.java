    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        // Obtengo los datos de los campos de texto
        datoCodigo = campoCodigo.getText().trim();
        datoNombre = campoNombre.getText();
        datoCondicion = campoCondicion.getText().trim();
        datoDescripcion = campoDescripcion.getText();
        datoNroCuit = campoNroCuit.getText().trim();
        datoDireccion = campoDireccion.getText();
        datoLocalidad = campoLocalidad.getText();
        datoContacto = campoContacto.getText();
        datoTipo = campoTipo.getText().trim();
        datoFechaOculta = campoFechaOculta.getText().trim();
        datoDiaModif = datoFechaOculta.charAt(0) + "" + datoFechaOculta.charAt(1);
        datoMesModif = datoFechaOculta.charAt(3) + "" + datoFechaOculta.charAt(4);
        datoAnoModif = datoFechaOculta.charAt(6) + "" + datoFechaOculta.charAt(7);

        // Continúa si ningún campo quedó sin datos o incompleto
        if (!datoCodigo.equals("") && !datoNombre.equals("") && datoCondicion.length() == 2 && !datoDescripcion.equals("")
                && datoNroCuit.length() == 13 && !datoDireccion.equals("") && !datoLocalidad.equals("")
                && !datoContacto.equals("") && !datoTipo.equals("")) {

            try {
                // Establece conexión con DB para hacer la consulta
                Connection con = Conexion.conectar();
                PreparedStatement sta = con.prepareStatement("select * from clientes where id = '" + ID + "'");
                ResultSet res = sta.executeQuery();

                if (res.next()) {
                    // Si ya fue creado, actualiza el resto de los datos
                    Connection con1 = Conexion.conectar();
                    PreparedStatement sta1 = con1.prepareStatement("update clientes set codigo=?, "
                            + "razon_social=?, condicion=?, descripcion=?, cuit=?, direccion=?, "
                            + "localidad=?, contacto=?, tipo=?, fecha_modif=?, hora_modif=?, dia_modif=?, mes_modif=?, "
                            + "ano_modif=? where id = '" + ID + "'");

                    sta1.setString(1, datoCodigo);
                    sta1.setString(2, datoNombre);
                    sta1.setString(3, datoCondicion);
                    sta1.setString(4, datoDescripcion);
                    sta1.setString(5, datoNroCuit);
                    sta1.setString(6, datoDireccion);
                    sta1.setString(7, datoLocalidad);
                    sta1.setString(8, datoContacto);
                    sta1.setString(9, datoTipo);
                    sta1.setString(10, datoFechaOculta);
                    sta1.setString(11, datoHoraActual);
                    sta1.setString(12, datoDiaModif);
                    sta1.setString(13, datoMesModif);
                    sta1.setString(14, datoAnoModif);

                    sta1.executeUpdate();
                    con1.close();
                    con.close();

                    try {
                        // Establece conexión con DB para hacer la consulta
                        Connection con2 = Conexion.conectar();
                        PreparedStatement sta2 = con2.prepareStatement("select codigo from ventas where codigo = '" + datoCodigo + "'");
                        ResultSet res2 = sta2.executeQuery();

                        if (res2.next()) {
                            // Si ya fue creado, actualiza el resto de los datos
                            Connection con3 = Conexion.conectar();
                            PreparedStatement sta3 = con3.prepareStatement("update ventas set codigo=?, razon_social=?, "
                                    + "condicion=? where codigo = '" + datoCodigo + "'");

                            sta3.setString(1, datoCodigo);
                            sta3.setString(2, datoNombre);
                            sta3.setString(3, datoCondicion);

                            sta3.executeUpdate();
                            con3.close();
                            con2.close();

                            // Muestra un mensaje si se actualizaron los datos correctamente
                            labelMensaje.setForeground(new java.awt.Color(0, 255, 0));
                            labelMensaje.setText("Cliente modificado exitosamente!");
                            labelMensaje.setVisible(true);

                            // Oculta botones, limpia campos, permite o no modificarlos y recupera el color de los labels
                            botonConsultar.setVisible(true);
                            botonModificar.setVisible(false);
                            botonGuardar.setVisible(false);

                            campoPorCodigo.setText("");
                            campoPorNombre.setText("");
                            campoPorNroCuit1.setText("");
                            campoPorNroCuit2.setText("");
                            campoPorNroCuit3.setText("");
                            campoCodigo.setText("");
                            campoNombre.setText("");
                            campoCondicion.setText("");
                            campoDescripcion.setText("");
                            campoNroCuit.setText("");
                            campoDireccion.setText("");
                            campoLocalidad.setText("");
                            campoContacto.setText("");
                            campoTipo.setText("");

                            campoPorCodigo.setEditable(true);
                            campoPorNombre.setEditable(true);
                            campoPorNroCuit1.setEditable(true);
                            campoPorNroCuit2.setEditable(true);
                            campoPorNroCuit3.setEditable(true);
                            campoCodigo.setRequestFocusEnabled(false);
                            campoNombre.setRequestFocusEnabled(false);
                            campoCondicion.setRequestFocusEnabled(false);
                            campoDescripcion.setRequestFocusEnabled(false);
                            campoNroCuit.setRequestFocusEnabled(false);
                            campoDireccion.setRequestFocusEnabled(false);
                            campoLocalidad.setRequestFocusEnabled(false);
                            campoContacto.setRequestFocusEnabled(false);
                            campoTipo.setRequestFocusEnabled(false);
                            
                            campoCodigo.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                            campoNombre.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                            campoCondicion.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                            campoDescripcion.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                            campoNroCuit.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                            campoDireccion.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                            campoLocalidad.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                            campoContacto.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
                            campoTipo.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));

                            campoPorCodigo.requestFocus();

                        } else {

                            con2.close();
                        }

                    } catch (SQLException e) {
                        // Si no se conecta con la DB, muestra un mensaje de error
                        JOptionPane.showConfirmDialog(null, "Error de conexión: Base ventas!", "Información",
                                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                    }

                } else {

                    con.close();
                }

            } catch (SQLException e) {
                // Si no se conecta con la DB, muestra un mensaje de error
                JOptionPane.showConfirmDialog(null, "Error de conexión: Base clientes!", "Información",
                        JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            }

        } else {
            // Si los campos están vacíos o incompletos, muestra un mensaje de error
            labelMensaje.setForeground(new java.awt.Color(255, 0, 0));
            labelMensaje.setText("Por favor, revise los datos ingresados!");
            labelMensaje.setVisible(true);

            campoCodigo.requestFocus();
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

