                    campoNroCuit.setText(res.getString("cuit"));
                    campoDireccion.setText(res.getString("direccion"));
                    campoLocalidad.setText(res.getString("localidad"));
                    campoContacto.setText(res.getString("contacto"));
                    campoTipo.setText(res.getString("tipo"));

                    // Oculta botones y el mensaje de error                    
                    botonConsultar.setVisible(false);
                    botonModificar.setVisible(true);
                    botonGuardar.setVisible(false);

