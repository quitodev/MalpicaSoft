    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        // Obtengo los datos de los campos de texto
        datoCodigo = campoCodigo.getText().trim();
        datoDescripcion = campoDescripcion.getText();
        datoCantidad = campoCantidad.getText().trim();
        datoMoneda = campoMoneda.getText().trim();
        datoPrecioUnit = campoPrecioUnit.getText().trim();
        datoFechaOculta = campoFechaOculta.getText().trim();
        datoDiaAlta = datoFechaOculta.charAt(0) + "" + datoFechaOculta.charAt(1);
        datoMesAlta = datoFechaOculta.charAt(3) + "" + datoFechaOculta.charAt(4);
        datoAnoAlta = datoFechaOculta.charAt(6) + "" + datoFechaOculta.charAt(7);

        // Convierto los importes de INT a STRING y viceversa
        cantidad = Integer.parseInt(datoCantidad);
        precioUnit = Double.parseDouble(datoPrecioUnit);
        precioTotal = cantidad * precioUnit;
        datoPrecioTotal = String.format("%.2f", precioTotal).replace(",", ".");

        // Si el código no fue creado, entonces procede a crearlo (siempre que los campos no estén vacíos)
        if (!datoCodigo.equals("") && !datoDescripcion.equals("") && !datoCantidad.equals("") && !datoMoneda.equals("")
                && datoMoneda.length() == 3 && !datoPrecioUnit.equals("") && datoPrecioUnit.contains(".")) {

            try {
                // Establece conexión con DB para consultar el código ingresado
                Connection con = Conexion.conectar();
                PreparedStatement sta = con.prepareStatement("select codigo_stock from stock where codigo_stock = '" + datoCodigo + "'");
                ResultSet res = sta.executeQuery();

                if (res.next()) {

                    // Si ya fue ingresado, muestra un mensaje de error
                    labelMensaje.setForeground(new java.awt.Color(255, 0, 0));
                    labelMensaje.setText("El código ingresado ya existe! Ingrese uno nuevo");
                    labelMensaje.setVisible(true);

                    campoCodigo.requestFocus();

                    con.close();

                } else {

                    try {
                        // Establece conexión con DB para registrar los datos ingresados
                        Connection con1 = Conexion.conectar();
                        PreparedStatement sta1 = con1.prepareStatement("insert into stock values (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");

                        sta1.setInt(1, 0);
                        sta1.setString(2, datoCodigo);
                        sta1.setString(3, datoDescripcion);
                        sta1.setString(4, datoCantidad);
                        sta1.setString(5, datoMoneda);
                        sta1.setString(6, datoPrecioUnit);
                        sta1.setString(7, datoPrecioTotal);
                        sta1.setString(8, datoFechaOculta);
                        sta1.setString(9, datoHoraActual);
                        sta1.setString(10, datoFechaOculta);
                        sta1.setString(11, datoHoraActual);
                        sta1.setString(12, datoDiaAlta);
                        sta1.setString(13, datoMesAlta);
                        sta1.setString(14, datoAnoAlta);
                        sta1.setString(15, datoDiaAlta);
                        sta1.setString(16, datoMesAlta);
                        sta1.setString(17, datoAnoAlta);

                        sta1.executeUpdate();
                        con1.close();

                        // Limpia los campos
                        campoCodigo.setText("");
                        campoDescripcion.setText("");
                        campoCantidad.setText("0");
                        campoMoneda.setText("ARS");
                        campoPrecioUnit.setText("0.00");

                        campoCodigo.requestFocus();

                        // Para terminar, muestra un mensaje para informar que se registraron correctamente los datos
                        labelMensaje.setForeground(new java.awt.Color(0, 255, 0));
                        labelMensaje.setText("Producto registrado exitosamente!");
                        labelMensaje.setVisible(true);

                    } catch (SQLException e) {
                        // Si no se conecta con la DB, muestra un mensaje de error
                        JOptionPane.showConfirmDialog(null, "Error de conexión: Base stock!", "Información", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                    }

                    con.close();
                }

            } catch (SQLException e) {
                // Si no se conecta con la DB, muestra un mensaje de error
                JOptionPane.showConfirmDialog(null, "Error de conexión: Base stock!", "Información", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
            }
            
        } else {
            // Si los campos están vacíos, muestra un mensaje de error
            labelMensaje.setForeground(new java.awt.Color(255, 0, 0));
            labelMensaje.setText("Por favor, revise los datos ingresados!");
            labelMensaje.setVisible(true);
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

    private void campoCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoCodigoKeyTyped
        // Restricción y limitación de caracteres
        char c = evt.getKeyChar();

        if (Character.isLowerCase(c)) {

            String cad = ("" + c).toUpperCase();
            c = cad.charAt(0);
            evt.setKeyChar(c);
        }

        if ((c < '0' || c > '9') && (c < 'a' || c > 'z') && (c < 'A' || c > 'Z')) {

            evt.consume();
        }

        if (campoCodigo.getText().length() >= 12) {

            evt.consume();
        }
    }//GEN-LAST:event_campoCodigoKeyTyped

