    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        datoCodigo = campoCodigo.getText().trim();

        // Muestra una ventana de confirmación antes de eliminar por completo
        int respuesta = JOptionPane.showConfirmDialog(null, "¿Desea eliminar el gasto? Esta acción no se puede revertir!", "Confirmación", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        switch (respuesta) {
            // Procede a eliminar el producto en caso de aceptar
            case JOptionPane.YES_OPTION:

                try {
                    // Establece conexión con DB para consultar por ID la factura a modificar
                    Connection con = Conexion.conectar();
                    PreparedStatement sta = con.prepareStatement("select * from gastos where id = '" + ID + "'");
                    ResultSet res = sta.executeQuery();

                    if (res.next()) {
                        // Obtengo la cantidad de la compra que se realizó para actualizar stock luego
                        datoCantidadCompra = res.getString("cantidad");
                        cantidadCompra = Integer.parseInt(datoCantidadCompra);

                        try {
                            // Establece conexión con DB para consultar el producto en la base de stock
                            Connection con1 = Conexion.conectar();
                            PreparedStatement sta1 = con1.prepareStatement("select * from stock where codigo_stock = '" + datoCodigo + "'");
                            ResultSet res1 = sta1.executeQuery();

                            if (res1.next()) {
                                // Si encuentra el código en la base de stock, actualiza el stock y la fecha de modif
                                datoCantidadVigente = res1.getString("cantidad");
                                datoPrecioUnitVigente = res1.getString("precio_unit");

                                cantidadVigente = Integer.parseInt(datoPrecioUnitVigente);
                                precioUnitVigente = Double.parseDouble(datoPrecioUnitVigente);

                                cantidadModificada = cantidadVigente - cantidadCompra;
                                datoCantidadModificada = cantidadModificada + "";
                                precioTotal = cantidadModificada * precioUnitVigente;
                                datoPrecioTotal = String.format("%.2f", precioTotal).replace(',', '.');

                                Connection con2 = Conexion.conectar();
                                PreparedStatement sta2 = con2.prepareStatement("update stock set cantidad=?, precio_total=? "
                                        + "where codigo_stock = '" + datoCodigo + "'");

                                sta2.setString(1, datoCantidadModificada);
                                sta2.setString(2, datoPrecioTotal);

                                sta2.executeUpdate();
                                con2.close();
                                con1.close();

                            } else {

                                con1.close();
                            }

                        } catch (SQLException e) {
                            // Si no se conecta con la DB, muestra un mensaje de error
                            JOptionPane.showConfirmDialog(null, "Error de conexión: Base stock!", "Información", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                        }

                        try {
                            // Establece conexión con DB utilizando el ID para consultar el código ingresado y eliminar la línea completa
                            Connection con1 = Conexion.conectar();
                            PreparedStatement sta1 = con1.prepareStatement("delete from gastos where id = ?");

                            sta1.setInt(1, ID);

                            // Muestra mensaje de borrado exitoso, oculta botones, limpia campos y recupera el color de los labels
                            labelMensaje.setForeground(new java.awt.Color(0, 255, 0));
                            labelMensaje.setText("El gasto fue eliminado exitosamente!");
                            labelMensaje.setVisible(true);

                            campoLetraComprob.setText("");
                            campoPuntoVenta.setText("");
                            campoNroComprob.setText("");
                            campoFechaCompra.setText("");
                            campoFechaIngresada.setText("");
                            campoCodigoProv.setText("");
                            campoNombreProv.setText("");
                            campoCondicionProv.setText("");
                            campoCodigo.setText("");
                            campoDescripcion.setText("");
                            campoCantidad.setText("");
                            campoPrecioUnit.setText("");
                            campoImpuestos.setText("");
                            campoPrecioFinal.setText("");

                            botonConsultar.setVisible(true);
                            botonEliminar.setVisible(false);

                            campoLetraComprob.requestFocus();

                            sta1.executeUpdate();

                            con1.close();

                        } catch (SQLException e) {
                            // Si no se conecta con la DB, muestra un mensaje de error
                            JOptionPane.showConfirmDialog(null, "Error de conexión: Base gastos!", "Información", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                        }

                    } else {

                        con.close();
                    }

                } catch (SQLException e) {
                    // Si no se conecta con la DB, muestra un mensaje de error
                    JOptionPane.showConfirmDialog(null, "Error de conexión: Base gastos!", "Información", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE);
                }
                break;

            // Si no acepta, no realiza ninguna acción
            case JOptionPane.NO_OPTION:
                break;

            // Si cierra la ventana, no realiza ninguna acción
            case JOptionPane.CLOSED_OPTION:
                break;

            default:
                break;
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

